package typecasting;

public class typecastingDivisionValueToInt {
    public static void main(String[] args) {
        int a=10;
        int b=3;
        int result = a/b;
        float r = (float)a/b;
        System.out.println("Result in Integer "+result);
        System.out.println("Result in Float "+r);
    }
}
